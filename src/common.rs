

use postgres_types::{FromSql, ToSql};
use serde::{Deserialize, Serialize};

#[derive(Debug, Deserialize, Serialize)]
pub struct Entity {
    pub name: String,
    pub urn: String,
}

#[derive(Debug, Deserialize, Serialize, ToSql, FromSql)]
pub enum EntityTypes {
    COMPANY_NAME,
    FIRMOGRAPHIC,
    COMPANY_SIZE,
    COMPANY_INDUSTRY,
    AGE,
    GENDER,
    LOCALE,
    DEGREE,
    SCHOOL,
    FIELD_OF_STUDY,
    FUNCTION,
    SENIORITY,
    TITLE,
    YEARS_OF_EXPERIENCE,
    INTEREST,
    SKILL,
    GROUP,
}

#[derive(Debug, Deserialize, Serialize)]
pub struct ParentEntity {
    #[serde(rename = "entityTypes")]
    pub entity_types: Vec<EntityTypes>,
    #[serde(rename = "facetName")]
    pub facet_name: String,
    #[serde(rename = "availableEntityFinders")]
    pub available_entity_finders: Vec<String>,
    pub children: Vec<Entity>,
    #[serde(rename = "facetUrn")]
    pub facet_urn: String,
}

